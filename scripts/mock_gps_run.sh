#!/bin/bash
# 서울 여의도 한강공원 러닝 경로 시뮬레이션
# 5:00/km 페이스 = 200m/분 = 3.33m/초
# 포인트 간 거리 약 50m → 50/3.33 ≈ 15초 간격

COORDS=(
  # lng lat (adb geo fix는 lng lat 순서)
  "126.9220 37.5268"
  "126.9225 37.5270"
  "126.9230 37.5272"
  "126.9236 37.5274"
  "126.9242 37.5276"
  "126.9248 37.5278"
  "126.9254 37.5280"
  "126.9260 37.5282"
  "126.9266 37.5284"
  "126.9272 37.5285"
  "126.9278 37.5286"
  "126.9284 37.5287"
  "126.9290 37.5288"
  "126.9296 37.5289"
  "126.9302 37.5290"
  "126.9308 37.5291"
  "126.9314 37.5292"
  "126.9320 37.5293"
  "126.9326 37.5294"
  "126.9332 37.5295"
  "126.9338 37.5296"
  "126.9344 37.5296"
  "126.9350 37.5296"
  "126.9356 37.5295"
  "126.9362 37.5294"
  "126.9368 37.5293"
  "126.9374 37.5292"
  "126.9380 37.5291"
  "126.9386 37.5290"
  "126.9392 37.5289"
  "126.9398 37.5288"
  "126.9404 37.5287"
  "126.9410 37.5286"
  "126.9416 37.5285"
  "126.9422 37.5284"
  "126.9428 37.5283"
  "126.9434 37.5282"
  "126.9440 37.5281"
  "126.9446 37.5280"
  "126.9452 37.5279"
)

INTERVAL=${1:-15}  # 기본 15초 간격 (5:00/km 페이스)

echo "🏃 러닝 시뮬레이션 시작 (여의도 한강공원)"
echo "총 ${#COORDS[@]} 포인트, ${INTERVAL}초 간격 (약 5:00/km 페이스)"
echo "총 예상 시간: $(( ${#COORDS[@]} * INTERVAL / 60 ))분"
echo ""

for i in "${!COORDS[@]}"; do
  coord="${COORDS[$i]}"
  lng=$(echo $coord | awk '{print $1}')
  lat=$(echo $coord | awk '{print $2}')

  adb emu geo fix $lng $lat 10 2>/dev/null

  echo "📍 [$((i+1))/${#COORDS[@]}] lat=$lat lng=$lng"

  if [ $((i+1)) -lt ${#COORDS[@]} ]; then
    sleep $INTERVAL
  fi
done

echo ""
echo "✅ 러닝 시뮬레이션 완료!"
