/**
 * Returns a daily quote that differs per user.
 * Same user sees the same quote all day, different users see different quotes.
 */
export function getDailyQuote(lang: string, userId?: string | null): string {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000,
  );
  // Simple hash from userId to spread users across quotes
  let userSeed = 0;
  if (userId) {
    for (let i = 0; i < userId.length; i++) {
      userSeed = ((userSeed << 5) - userSeed + userId.charCodeAt(i)) | 0;
    }
  }
  const index = Math.abs(dayOfYear + userSeed) % QUOTES.length;
  const quote = QUOTES[index];
  if (lang.startsWith('ja')) return quote.ja;
  if (lang.startsWith('en')) return quote.en;
  return quote.ko;
}
